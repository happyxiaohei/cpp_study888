# include<iostream>
using namespace std;

//静态成员变量   关键字为   static

class Person8701
{
	//1.所有对象都共享同一份数据
	//2.编译阶段就分配内存
	//3.类内声明,类外初始化操作
public:

	static int m_A;


	//静态成员变量 也是有访问权限的

private:
	static int m_B;

};

//这一步代表   类内声明,类外初始化操作
int Person8701::m_A = 100;
int Person8701::m_B = 200;

void taxt8701()
{

	Person8701 p;
	cout << p.m_A << endl;

	Person8701 p2;
	p2.m_A = 200;
	cout << p.m_A << endl;

}

