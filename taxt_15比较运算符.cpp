# include<iostream>
using namespace std;

int main15() {

	//比较运算符

	//==   相等于
	//!=   不等于
	//<  小于
	// >  大于
	// <=  小于等于
	// >=   大于等于

	int a = 10;
	int b = 20;
	cout << "相等于" << (a == b) << endl;
	cout << "不等于" << (a != b) << endl;
	cout << "小于" << (a < b) << endl;
	cout << "大于" << (a > b) << endl;
	cout << "小于等于" << (a <= b) << endl;
	cout << "大于等于" << (a >= b) << endl;

	//  0 代表假   1 代表真





	system("pause");

	return 0;

}