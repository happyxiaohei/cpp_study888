# include<iostream>
using namespace std;

int main14() {

	//赋值运算符
	// = += -= *= /= %=
	//一共是有这五种
	int a = 10;
	cout << "a = " << a << endl;

	a = 10;
	a += 2;
	cout << "a += " << a << endl;


	a = 10;
	a -= 2;
	cout << "a -= " << a << endl;


	a = 10;
	a *= 2;
	cout << "a *= " << a << endl;


	a = 10;
	a /= 2;
	cout << "a /= " << a << endl;


	a = 10;
	a %= 2;
	cout << "a %= " << a << endl;
	system("pause");

	return 0;

}